    private void displayCar() {
        txtBrand.setText(car.getBrand());
        txtModel.setText(car.getModel());
        txtColor.setText(car.getColor());
        txtYear.setText(car.getYear());
        txtEngineNo.setText(car.getEngineNo());
        txtSeatsNumber.setText(car.getSeatsNumber());
        txtLicensePlates.setText(car.getLicensePlates());
        txtOwnerName.setText(car.getOwnerName());
        txtOwnerTelephoneNumbers.setText(car.getOwnerTelephoneNumbers());        
        txtOwnerEmailAddresses.setText(car.getOwnerEmailAddresses());
        txtOwnerDriverLicense.setText(car.getOwnerDriverLicense());
        txtOwnerSocialSecurityNumber.setText(car.getOwnerSocialSecurityNumber());
        txtOwnerAddress.setText(car.getOwnerAddress());
        txtWarrantyYear.setText(car.getWarrantyYear());

        //Logic to view service record
        DefaultTableModel model= (DefaultTableModel) tblServiceRecordsHistory.getModel();
        model.setRowCount(0);
        for(ServiceRecords sr: car.getServiceRecordsHistory().getHistory()){
            Object[] row= new Object[3];
            row[0]= sr.getId();
            row[1]=sr.getDate();
            row[2]=sr.getMileage();
            
            model.addRow(row);
        }
        
        //Logic to add car image
        photo.setIcon(setIconImage(car.getjPhoto(), null));
                
    }
    
