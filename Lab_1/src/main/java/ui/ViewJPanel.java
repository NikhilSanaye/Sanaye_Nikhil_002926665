    ImageIcon myImage;
    public ImageIcon setIconImage(String imgPath, byte[] image){
        if(imgPath!=null){
            myImage= new ImageIcon(imgPath);
        } else{
            myImage= new ImageIcon(image);
        }
        Image img1= myImage.getImage();
        Image img2= img1.getScaledInstance(100, 100, Image.SCALE_SMOOTH);
        ImageIcon i= new ImageIcon(img2);
        return i;        
    }   
