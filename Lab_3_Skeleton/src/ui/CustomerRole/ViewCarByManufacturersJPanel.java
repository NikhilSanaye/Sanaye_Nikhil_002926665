    public void refreshTable(){
        int rowCount = tblCarDetailsByManufacturer.getRowCount();
        DefaultTableModel model = (DefaultTableModel) tblCarDetailsByManufacturer.getModel();
        for(int i=rowCount-1;i>=0;i--){
            model.removeRow(i);
        }
        for (Car s : carList) {
            Object row[] = new Object[8];
            row[0] = s.getManufacturer();
            row[1] = s.getSerialNumber();
            row[2] = s.getModelNumber();
            row[3] = s.getManufacturingYear();    
            row[4] = s.getCity();
            row[5] = s.getNumberOfSeats();
            row[6] = s.getMaintenanceCertificate();
            if(Boolean. valueOf(s.getAvailability())){
                row[7] = "Yes";
            } else{
                row[7] = "No";
            }
            model.addRow(row);
        }
    }

